export const getLeadsStats = () => {
  const total = leads.length;
  const byStatus = {
    new: leads.filter((l) => l.status === LeadStatus.NEW).length,
    contacted: leads.filter((l) => l.status === LeadStatus.CONTACTED).length,
    qualified: leads.filter((l) => l.status === LeadStatus.QUALIFIED).length,
    proposal: leads.filter((l) => l.status === LeadStatus.PROPOSAL).length,
    negotiation: leads.filter((l) => l.status === LeadStatus.NEGOTIATION).length,
    won: leads.filter((l) => l.status === LeadStatus.WON).length,
    lost: leads.filter((l) => l.status === LeadStatus.LOST).length,
  };
  const byTemperature = {
    hot: leads.filter((l) => l.temperature === LeadTemperature.HOT).length,
    warm: leads.filter((l) => l.temperature === LeadTemperature.WARM).length,
    cold: leads.filter((l) => l.temperature === LeadTemperature.COLD).length,
  };
  const totalValue = leads.reduce((sum, l) => sum + (l.estimatedValue || 0), 0);
  const wonValue = leads
    .filter((l) => l.status === LeadStatus.WON)
    .reduce((sum, l) => sum + (l.estimatedValue || 0), 0);

  return {
    total,
    byStatus,
    byTemperature,
    totalValue,
    wonValue,
    conversionRate: total > 0 ? (byStatus.won / total) * 100 : 0,
  };
};
