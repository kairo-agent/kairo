// Helper to get lead by ID
export const getLeadById = (id: string): Lead | undefined => {
  return leads.find((lead) => lead.id === id);
};

// Get leads by status
export const getLeadsByStatus = (status: LeadStatus): Lead[] => {
  return leads.filter((lead) => lead.status === status);
};

// Get leads by temperature
export const getLeadsByTemperature = (temperature: LeadTemperature): Lead[] => {
  return leads.filter((lead) => lead.temperature === temperature);
};

// Get leads by channel
export const getLeadsByChannel = (channel: LeadChannel): Lead[] => {
  return leads.filter((lead) => lead.channel === channel);
};

// Get leads by agent
export const getLeadsByAgent = (agentId: string): Lead[] => {
  return leads.filter((lead) => lead.assignedAgentId === agentId);
};

// Get leads by user
export const getLeadsByUser = (userId: string): Lead[] => {
  return leads.filter((lead) => lead.assignedUserId === userId);
};

